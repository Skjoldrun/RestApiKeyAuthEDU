using Microsoft.AspNetCore.Mvc;
using RestApiKeyAuthEDU.Authentication;

namespace RestApiKeyAuthEDU.Controllers
{
    [ApiController] // [ServiceFilter(typeof(ApiKeyAuthFilter))] // This would activate the filtering for all controller endpoints
    public class WeatherForecastController : ControllerBase
    {
        public static readonly string[] Summaries = new[]
        {
        "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
    };

        private readonly ILogger<WeatherForecastController> _logger;

        public WeatherForecastController(ILogger<WeatherForecastController> logger)
        {
            _logger = logger;
        }

        [HttpGet("Weather")]
        [ServiceFilter(typeof(ApiKeyAuthFilter))]  // This activates the AuthKeyFilter for this endpoint
        public IEnumerable<WeatherForecast> Get()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpGet("Weather2")] // This is the open alternative without API key filtering unless it is activated for the whole controller
        public IEnumerable<WeatherForecast> GetOpen()
        {
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateOnly.FromDateTime(DateTime.Now.AddDays(index)),
                TemperatureC = Random.Shared.Next(-20, 55),
                Summary = Summaries[Random.Shared.Next(Summaries.Length)]
            })
            .ToArray();
        }
    }
}